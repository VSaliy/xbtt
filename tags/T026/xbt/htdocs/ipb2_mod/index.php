<h2>Overview</h2>

XBT IPB2 Mod is a mod for Invision Power Board 2 and it's a frontend for XBT Tracker.<br>
In forum views, it'll show statistics (seeders and leechers) for each topic that contains a .torrent attachment.<br>
In topic views, it'll show statistics (seeders and leechers) for each .torrent attachment<br>
The modified HTML templates are easily editable via the standard IPB2 template editor in the admin control panel, so you can modify the layout of the statistics.<br>

